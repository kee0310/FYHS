          <p data-aos="fade-up">
            南马的宽柔中学与北马的韩江中学终于在2011年11月17日缔结为姐妹学校。这在独立中学来说，倒是历史性的“两结合”。
            正如韩中董事长陈国平所说的，历史的渊源将这两间名校联系在一起而在今日擦出教育的火花。也正如宽中董事长陈伟雄所形容的，
            彼此也完成了一个华文教育体系的使命，准备在未来的道路上携手前进。
            当然这也正如宽中赠送韩中的一幅对联所说的：“情谊历久弥新，共创美好未来”。<br><br>
          </p>

          <img data-aos="fade-up" src="img/pi14.jpg" width="50%" style="float: right; padding: 50px; min-width: 400px">

          <p data-aos="fade-up">
            本来韩中与宽中是各自发展的，也一向以来未曾直接的交流，因为彼此相隔太远，也就各就各位。
            但踏入今年以来，韩江中学生4月迎来了宽中第一批客人。在参观之后，双方建立起朴实的教育情。
            接着在5月的时候，我们又接待宽中第二批董教代表团一行廿人到来参观访问。
            在第二副董事长童存星的率领下，成员包括宽中领导董事与正副校长，阵容比第一次到访还要整齐。
            因为有了良好的开始，韩中董教仝人决定组团回报访问（9月份），全团一共20余人“浩浩荡荡”向宽中出发。
            承蒙宽中董教仝人的热情接待和介绍，使我们对宽中的办学与校风有更进一步的理解和感受。
          </p>
          <br><br>

          <p data-aos="fade-up">
            在参观宽中总校和古来分校后，我们的印象是“大而宽”，不仅总校在新山市区拔地而起，成为柔佛最著名的华文中学；
            更在2002年在古来增设新校区，占地30依格。当2008年古来分校全面使用时，这间独立中学已成为全马最大的独立中学，分开两个校区。
            即使总校占地比不上分校（约13.5依格），但它的古老而温馨的校园让人身置其中，有无限的遐想。
            而在古来的校园，建筑富丽堂皇，设置有如学院，别有洞天，实在是“百年树人”的好场所。
          </p>
          <br><br>

          <p data-aos="fade-up">
            虽然名为“分校”，但我被告知，其实宽中是一体的，只是分开两个校园，形成了一校两园的美丽风景线。
            而有“校本部”和“分校”的称呼是为了方便。
          </p>
          <br><br>

          <p data-aos="fade-up">
            至于古来分校，它宛如一间有水准的学院，学生在这里受教育肯定会因舒适的环境而变得更加聪明和勤力。
            根据校方告知，今年宽中共有学生超过9600名，明年则突破一万名。由于学校设备难以容纳，因此有逾千名的小六生不得其门而入。
            这就是说，宽中已成为柔佛华社的希望和学问的殿堂，许许多多的家长都渴望孩子进入宽中而骄傲，但是宽中还有一个鲜为人知的特点，
            它的前任董事长林俊民这样告诉我，他的孙子因为成绩少了几分而考不进宽柔中学，只好将他安排在其他中学受教育。
            虽然身为校董，他没有特权，宽中不搞特殊化，也不设特殊班，一切以考试成绩为标准。
            因此宽中给人留下的好印象是：它是大公无私的；在有教无类下，概由成绩来说话。
          </p>
          <br><br>

          <p data-aos="fade-up">
            是的，宽中大礼堂前的广场竖立的孔子铜像已彰显宽中的教育精神，万世师表的孔子也成了千千万万学子崇拜与学习的对象。
            因为宽中的成功，很多人都以此作为独中办学成功的典范和榜样，但独中的命运和际遇是因地域而各不相同的，
            也不能相提并论，甚至不可同日而语。
          </p>
